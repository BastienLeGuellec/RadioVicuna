    is_t5 = "t5" in str(type(model)).lower()

    # Hardcode T5's default repetition penalty to be 1.2
    if is_t5 and repetition_penalty == 1.0:
        repetition_penalty = 1.2


    conv = get_conv_template(conv_template)


    f = open(file_path, 'r')
    inputlist = f.read()

